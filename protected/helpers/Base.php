    public static function file_size($size) {

        $filesizename = array(" Bytes", " Кб", " Мб", " Гб", " TB", " PB", " EB", " ZB", " YB");
        return $size ? round($size/pow(1024, ($i = floor(log($size, 1024)))), 2) . $filesizename[$i] : '0 Bytes';
    }

